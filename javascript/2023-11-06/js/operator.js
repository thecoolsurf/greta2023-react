/* ************************************************************* */
/* ARITHMETIC OPERATOR */

let score = 12;

// add bonus to score
score = score + 10; // -> 22
score += 10; // -> 32
score -= 5; // -> 27
score *= 2; // ->54
score /= 3; // -> 18

/* ************************************************************* */
/* INCREMENTATION */

score++; // -> 19
score--; // -> 18

